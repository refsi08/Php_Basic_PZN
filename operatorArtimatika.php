<?php 

$a = 10;
$b = 15;

$result = $a + $b; // Penjumlahan
var_dump($result);
echo $result;

$result = $a - $b; // Pengurangan
var_dump($result);
echo $result;

$resultPositif = +$result; // Positif
var_dump($resultPositif);
echo $resultPositif;

$resultNegatif = -$result; // Negatif
var_dump($resultNegatif);
echo $resultNegatif;

$resultModulo = 100 % 3; // Perpangkatan Modulo
var_dump($resultModulo);
echo $resultModulo;

?>